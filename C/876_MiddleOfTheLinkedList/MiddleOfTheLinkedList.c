#define NULL (void*)0

struct ListNode* middleNode(struct ListNode* head){

    struct ListNode *cur = head; 
    int num = -1;

    for(; head != NULL; head = head->next){

        if(++num % 2 == 0)
            continue;
        cur = cur->next;
    }

    return cur;
}